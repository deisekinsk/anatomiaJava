/**
 * || Critérios para construção dos métodos
 * Métodos são para definir funções ou sub-rotinas
 * 
 * Qual a proposta do método?
 * Tem retorno? Qual?
 * Tem parametros? Quais?
 * Tem exceção? Quais?
 * Qual a visibilidade do método?
 * 
 * Qual a proposta principal do método?
 * Qual o tipo de retorno esperado?
 * Quais parametros necessários para execução?
 * O método possui risco de apresentar alguma exceção?
 * Qual a visibilidade do método?
 * 
 */

 public class Metodos {
         public double somar (int numA, int numB){
           return numA + numB;
         }

         public double dividir (int dividendo, int divisor) {
            return dividendo/divisor;
         }

}